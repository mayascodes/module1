#defining each data type
integer = 42
float = 9.81
string = "My name is Maya"
boolean = True
list = ["apple", "strawberry", "cherry"]
tuple = (5, 10, 15)
dictionary = {"name": "Maya", "age": 16}
set = {"blue", "red", "green"}

#printing each variable
print("integer:", integer, "data type: integer")
print("float:", float, "data type: float")
print("string:", string, "data type: string")
print("boolean:", boolean, "data type: boolean")
print("list:", list, "data type: list")
print("tuple:", tuple, "data type: tuple")
print("dictionary:", dictionary, "data type: dictionary")
print("set:", set, "data type: set")